<?php 
    $score = 50;

    //ถ้าคะแนนมากกว่า 50 ให้แสดง เกรด D
    //ถ้าคะแนนมากกว่า 60 ให้แสดง เกรด C
    //ถ้าคะแนนมากกว่า 70 ให้แสดง เกรด B
    //ถ้าคะแนนมากกว่า 80 ให้แสดง เกรด A
    //ถ้าคะแนนต่ำกว่า 50 ให้แสดง เกรด F

    if($score>50){
        echo "เกรด D";
    }elseif($score<=60){
        echo "เกรด C";
    }elseif($score<=70){
        echo "เกรด B";
    }elseif($score<=80){
        echo "เกรด A";
    }else{
        echo "เกรด F";
    }
?>